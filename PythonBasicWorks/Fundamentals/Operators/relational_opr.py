# Relational operator ( < , <= , > , >= , == , != ) this returns only boolean values

num1=100

num2=200

print(num1<num2) #100<200 True Which means num1 less than num2

print(num1>num2) #100>200 False

print(num1<=num2) #100<=200 True

print(num1>=num2) #100>=200 False

print(num1==num2) #100==200 False

print(num1!= 0) #100!= 0 True
