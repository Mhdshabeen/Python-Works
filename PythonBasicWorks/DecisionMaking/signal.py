# write  program to print the signal type [green,orange,red]


signal=input("Enter the signal color :> ")

if (signal=="green"):

    print(f" go if color is green ") 

elif (signal=="red"):

    print(f"Stop if the signal is red ")

elif (signal=="orange"):

    print(f"Ready if the signal is orange")

else:

    print(f"invalid ")


